package com.sat.donatarias.model;

public class TareasPendientes {
	

	private String numeroAsunto;
	private String numeroExpendiente;
	private String estadoProcesal;
	private String tipoSolicitud;
	private String rfc;
	
	public String getNumeroAsunto() {
		return numeroAsunto;
	}
	public void setNumeroAsunto(String numeroAsunto) {
		this.numeroAsunto = numeroAsunto;
	}
	public String getNumeroExpendiente() {
		return numeroExpendiente;
	}
	public void setNumeroExpendiente(String numeroExpendiente) {
		this.numeroExpendiente = numeroExpendiente;
	}
	public String getEstadoProcesal() {
		return estadoProcesal;
	}
	public void setEstadoProcesal(String estadoProcesal) {
		this.estadoProcesal = estadoProcesal;
	}
	public String getTipoSolicitud() {
		return tipoSolicitud;
	}
	public void setTipoSolicitud(String tipoSolicitud) {
		this.tipoSolicitud = tipoSolicitud;
	}
	public String getRfc() {
		return rfc;
	}
	public void setRfc(String rfc) {
		this.rfc = rfc;
	}
	

	
	


}
