package com.sat.donatarias.model;

public class ModificarDatosExpedienteAdministradorAbogado {

	private String rfcContribuyente;
    private String denominacionRazonSocil; 
	private String numeroExpediente;
	
	public String getRfcContribuyente() {
		return rfcContribuyente;
	}
	public void setRfcContribuyente(String rfcContribuyente) {
		this.rfcContribuyente = rfcContribuyente;
	}
	public String getDenominacionRazonSocil() {
		return denominacionRazonSocil;
	}
	public void setDenominacionRazonSocil(String denominacionRazonSocil) {
		this.denominacionRazonSocil = denominacionRazonSocil;
	}
	public String getNumeroExpediente() {
		return numeroExpediente;
	}
	public void setNumeroExpediente(String numeroExpediente) {
		this.numeroExpediente = numeroExpediente;
	}
 
	 
	
	
}
