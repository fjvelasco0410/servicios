package com.sat.donatarias.model;

public class DTOTipoSolicitud {
	
	private int id; 
	private String descripcion;
	private String tipo; 
	private String tipoCompleto; 

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getTipoCompleto() {
		return tipoCompleto;
	}

	public void setTipoCompleto(String tipoCompleto) {
		this.tipoCompleto = tipoCompleto;
	} 
	
	
	
	

}
