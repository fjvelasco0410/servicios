package com.sat.donatarias.model;

public class EstadoProcesal {
	
	private int idEstdoProcesal;
	private String descripcion;
	
	public int getIdEstdoProcesal() {
		return idEstdoProcesal;
	}
	
	public void setIdEstdoProcesal(int idEstdoProcesal) {
		this.idEstdoProcesal = idEstdoProcesal;
	}
	
	public String getDescripcion() {
		return descripcion;
	}
	
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

}
